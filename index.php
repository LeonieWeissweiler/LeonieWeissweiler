          <div class="collapse" id="cistem_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>Stemmers, which reduce words to their stems, are important components
                of many natural language processing systems. In this paper, we conduct
                a systematic evaluation of several stemmers for German using two gold standards
                we have created and will release to the community. We then present our
                own stemmer, which achieves state-of-the-art results, is easy to understand
                and extend, and will be made publicly available both for use by programmers
                and as a benchmark for further stemmer development.</small>
            </div>
