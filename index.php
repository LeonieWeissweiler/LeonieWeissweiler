          <b>Leonie Weissweiler</b>, Valentin Hofmann, Abdullatif Köksal, Hinrich Schütze (2022). <i>The Better Your Syntax, the Better Your Semantics? Probing Pretrained Language Models for the English Comparative Correlative</i> . Proceedings of the 2022 Conference on Empirical Methods in Natural Language Processing, pages 10859–10882, Abu Dhabi, United Arab Emirates. Association for Computational Linguistics. (<b>EMNLP</b>) <br>
          <a id="cc_abstract_button" data-bs-toggle="collapse" href="#cc_abstract" role="button" aria-expanded="false" aria-controls="cc_abstract">Abstract</a>
          <a href="https://preview.aclanthology.org/emnlp-22-ingestion/2022.emnlp-main.746.pdf" target="_blank">PDF</a>
          <a href="https://github.com/LeonieWeissweiler/ComparativeCorrelative" target="_blank">Source code on Github <img src="util/images/octocat.png" height="12"></a>
          <br>
          <div class="collapse" id="cc_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>Construction Grammar (CxG) is a paradigm from cognitive linguistics emphasising the connection between syntax and semantics. Rather than rules that operate on lexical items, it posits constructions as the central building blocks of language, i.e., linguistic units of different granularity that combine syntax and semantics. As a first step towards assessing the compatibility of CxG with the syntactic and semantic knowledge demonstrated by state-of-the-art pretrained language models (PLMs), we present an investigation of their capability to classify and understand one of the most commonly studied constructions, the English comparative correlative (CC). We conduct experiments examining the classification accuracy of a syntactic probe on the one hand and the models' behaviour in a semantic application task on the other, with BERT, RoBERTa, and DeBERTa as the example PLMs. Our results show that all three investigated PLMs are able to recognise the structure of the CC but fail to use its meaning. While human-like performance of PLMs on many NLP tasks has been alleged, this indicates that PLMs still suffer from substantial shortcomings in central domains of linguistic knowledge.</small>
            </div>
          </div>
          
          <br>

          <b>Leonie Weissweiler</b>, Valentin Hofmann, Masoud Jalili Sabet, Hinrich Schütze (2022). <i>CaMEL: Case Marker Extraction without Labels</i>. Proceedings of the 60th Annual Meeting of the Association for Computational Linguistics (Volume 1: Long Papers), pages 5506–5516, Dublin, Ireland. Association for Computational Linguistics (<b>ACL</b>) <br>
          <a id="camel_abstract_button" data-bs-toggle="collapse" href="#camel_abstract" role="button" aria-expanded="false" aria-controls="camel_abstract">Abstract</a>
          <a href="https://aclanthology.org/2022.acl-long.377.pdf" target="_blank">PDF</a>
          <a href="https://aclanthology.org/2022.acl-long.377.bib">BibTeX</a>
          <a href="https://github.com/LeonieWeissweiler/CaMEL" target="_blank">Source code on Github <img src="util/images/octocat.png" height="12"></a>
          <br>
          <div class="collapse" id="camel_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>We introduce CaMEL (Case Marker Extraction without Labels), a novel and challenging task in computational morphology that is especially relevant for low-resource languages. We propose a first model for CaMEL that uses a massively multilingual corpus to extract case markers in 83 languages based only on a noun phrase chunker and an alignment system. To evaluate CaMEL, we automatically construct a silver standard from UniMorph. The case markers extracted by our model can be used to detect and visualise similarities and differences between the case systems of different languages as well as to annotate fine-grained deep cases in languages in which they are not overtly marked.</small>
            </div>
          </div>
          
          <br>

