          Yihong Liu, Haotian Ye, <b>Leonie Weissweiler</b>, Philipp Wicke, Renhao Pei, Robert Zangenfeind, Hinrich Schütze (2023). <i>A Crosslingual Investigation of Conceptualization in 1335 Languages</i>. Proceedings of the 61th Annual Meeting of the Association for Computational Linguistics. (<b>ACL</b>)<br>
          <a id="yihong_abstract_button" data-bs-toggle="collapse" href="#yihong_abstract" role="button" aria-expanded="false" aria-controls="yihong_abstract">Abstract</a>
          <a href="https://arxiv.org/pdf/2305.08475.pdf" target="_blank">PDF</a>
          <br>
          <div class="collapse" id="yihong_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>Languages differ in how they divide up the world into concepts and words; e.g., in contrast to English, Swahili has a single concept for `belly' and `womb'. We investigate these differences in conceptualization across 1,335 languages by aligning concepts in a parallel corpus. To this end, we propose Conceptualizer, a method that creates a bipartite directed alignment graph between source language concepts and sets of target language strings. In a detailed linguistic analysis across all languages for one concept (`bird') and an evaluation on gold standard data for 32 Swadesh concepts, we show that Conceptualizer has good alignment accuracy. We demonstrate the potential of research on conceptualization in NLP with two experiments. (1) We define crosslingual stability of a concept as the degree to which it has 1-1 correspondences across languages, and show that concreteness predicts stability. (2) We represent each language by its conceptualization pattern for 83 concepts, and define a similarity measure on these representations. The resulting measure for the conceptual similarity of two languages is complementary to standard genealogical, typological, and surface similarity measures. For four out of six language families, we can assign languages to their correct family based on conceptual similarity with accuracy between 54% and 87%.</small>
            </div>
          </div>

