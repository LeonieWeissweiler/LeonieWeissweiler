        <h3>Publications</h3>

        <b>Leonie Weissweiler</b>, Nina Böbel, Kirian Guiller, Santiago Herrera, Wesley Scivetti, Arthur Lorenzi, Nurit Melnik, Archna Bhatia, Hinrich Schütze, Lori Levin, Amir Zeldes, Joakim Nivre, William Croft, Nathan Schneider (2024). <i>UCxn: Typologically Informed Annotation of Constructions Atop Universal Dependencies</i>. Proceedings of the 2024 Joint International Conference on Computational Linguistics, Language Resources and Evaluation. (<b>LREC-COLING</b>)<br>
          <a id="ucxn_abstract_button" data-bs-toggle="collapse" href="#ucxn_abstract" role="button" aria-expanded="false" aria-controls="ucxn_abstract">Abstract</a>
          <a href="https://arxiv.org/pdf/2403.17748" target="_blank">PDF</a>
          <br>
          <div class="collapse" id="ucxn_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>The Universal Dependencies (UD) project has created an invaluable collection of treebanks with contributions in over 140 languages. However, the UD annotations do not tell the full story. Grammatical constructions that convey meaning through a particular combination of several morphosyntactic elements -- for example, interrogative sentences with special markers and/or word orders -- are not labeled holistically. We argue for (i) augmenting UD annotations with a 'UCxn' annotation layer for such meaning-bearing grammatical constructions, and (ii) approaching this in a typologically informed way so that morphosyntactic strategies can be compared across languages. As a case study, we consider five construction families in ten languages, identifying instances of each construction in UD treebanks through the use of morphosyntactic patterns. In addition to findings regarding these particular constructions, our study yields important insights on methodology for describing and identifying constructions in language-general and language-particular ways, and lays the foundation for future constructional enrichment of UD treebanks.</small>
            </div>
          </div>

        <br>

        Shijia Zhou, <b>Leonie Weissweiler</b>, Taiqi He, Hinrich Schütze, David Mortensen, Lori Levin (2024). <i>Constructions Are So Difficult That Even Large Language Models Get Them Right for the Wrong Reasons</i>. Proceedings of the 2024 Joint International Conference on Computational Linguistics, Language Resources and Evaluation. (<b>LREC-COLING</b>)<br>
        <a id="so_that_abstract_button" data-bs-toggle="collapse" href="#so_that_abstract" role="button" aria-expanded="false" aria-controls="so_that_abstract">Abstract</a>
        <a href="https://arxiv.org/pdf/2403.17760" target="_blank">PDF</a>
        <br>
        <div class="collapse" id="so_that_abstract" style="padding-top: 20px">
          <div class="card card-body">
            <small>In this paper, we make a contribution that can be understood from two perspectives: from an NLP perspective, we introduce a small challenge dataset for NLI with large lexical overlap, which minimises the possibility of models discerning entailment solely based on token distinctions, and show that GPT-4 and Llama 2 fail it with strong bias. We then create further challenging sub-tasks in an effort to explain this failure. From a Computational Linguistics perspective, we identify a group of constructions with three classes of adjectives which cannot be distinguished by surface features. This enables us to probe for LLM's understanding of these constructions in various ways, and we find that they fail in a variety of ways to distinguish between them, suggesting that they don't adequately represent their meaning or capture the lexical properties of phrasal heads.</small>
          </div>
        </div>

        <br>

        David Mortensen, Valentina Izrailevitch, Yunze Xiao, Hinrich Schütze, <b>Leonie Weissweiler</b> (2024). <i>Counting the Bugs in ChatGPT's Wugs: A Multilingual Investigation into the Morphological Capabilities of a Large Language Model</i>. Proceedings of the 2023 Conference on Empirical Methods in Natural Language Processing. (<b>LREC-COLING</b>)<br>
        <a id="verbing_button" data-bs-toggle="collapse" href="#verbing_abstract" role="button" aria-expanded="false" aria-controls="verbing_abstract">Abstract</a>
        <a href="https://arxiv.org/pdf/2403.17856.pdf" target="_blank">PDF</a>
        <br>
        <div class="collapse" id="verbing_abstract" style="padding-top: 20px">
          <div class="card card-body">
            <small>Lexical-syntactic flexibility, in the form of conversion (or zero-derivation) is a hallmark of English morphology. In conversion, a word with one part of speech is placed in a non-prototypical context, where it is coerced to behave as if it had a different part of speech. However, while this process affects a large part of the English lexicon, little work has been done to establish the degree to which language models capture this type of generalization. This paper reports the first study on the behavior of large language models with reference to conversion. We design a task for testing lexical-syntactic flexibility—the degree to which models can generalize over words in a construction with a non-prototypical part of speech. This task is situated within a natural language inference paradigm. We test the abilities of five language models—two proprietary models (GPT-3.5 and GPT-4), three open-source models (Mistral 7B, Falcon 40B, and Llama 2 70B). We find that GPT-4 performs best on the task, followed by GPT-3.5, but that the open source language models are also able to perform it and that the 7B parameter Mistral displays as little difference between its baseline performance on the natural language inference task and the non-prototypical syntactic category task, as the massive GPT-4.</small>
          </div>
        </div>

        <br>

        <b>Leonie Weissweiler*</b>, Valentin Hofmann*, Anjali Kantharuban, Anna Cai, Ritam Dutt, Amey Hengle, Anubha Kabra, Atharva Kulkarni, Abhishek Vijayakumar, Haofei Yu, Hinrich Schuetze, Kemal Oflazer, David Mortensen (2023). <i>Counting the Bugs in ChatGPT's Wugs: A Multilingual Investigation into the Morphological Capabilities of a Large Language Model</i>. Proceedings of the 2023 Conference on Empirical Methods in Natural Language Processing. (<b>EMNLP</b>)<br>
          <a id="wug_abstract_button" data-bs-toggle="collapse" href="#wug_abstract" role="button" aria-expanded="false" aria-controls="wug_abstract">Abstract</a>
          <a href="https://arxiv.org/ftp/arxiv/papers/2310/2310.15113.pdf" target="_blank">PDF</a>
          <br>
          <div class="collapse" id="wug_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>Large language models (LLMs) have recently reached an impressive level of linguistic capability, prompting comparisons with human language skills. However, there have been relatively few systematic inquiries into the linguistic capabilities of the latest generation of LLMs, and those studies that do exist (i) ignore the remarkable ability of humans to generalize, (ii) focus only on English, and (iii) investigate syntax or semantics and overlook other capabilities that lie at the heart of human language, like morphology. Here, we close these gaps by conducting the first rigorous analysis of the morphological capabilities of ChatGPT in four typologically varied languages (specifically, English, German, Tamil, and Turkish). We apply a version of Berko's (1958) wug test to ChatGPT, using novel, uncontaminated datasets for the four examined languages. We find that ChatGPT massively underperforms purpose-built systems, particularly in English. Overall, our results -- through the lens of morphology -- cast a new light on the linguistic capabilities of ChatGPT, suggesting that claims of human-like language skills are premature and misleading.</small>
            </div>
          </div>

          <br>

        Yihong Liu, Haotian Ye, <b>Leonie Weissweiler</b>, Hinrich Schütze (2023). <i>Crosslingual Transfer Learning for Low-Resource Languages Based on Multilingual Colexification Graphs</i>. Findings of the 2023 Conference on Empirical Methods in Natural Language Processing. (<b>EMNLP</b>)<br>
          <a id="yihong_2_abstract_button" data-bs-toggle="collapse" href="#yihong_2_abstract" role="button" aria-expanded="false" aria-controls="yihong_2_abstract">Abstract</a>
          <a href="https://arxiv.org/pdf/2305.12818.pdf" target="_blank">PDF</a>
          <br>
          <div class="collapse" id="yihong_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>Colexification in comparative linguistics refers to the phenomenon of a lexical form conveying two or more distinct meanings. In this paper, we propose simple and effective methods to build multilingual graphs from colexification patterns: ColexNet and ColexNet+. ColexNet's nodes are concepts and its edges are colexifications. In ColexNet+, concept nodes are in addition linked through intermediate nodes, each representing an ngram in one of 1,334 languages. We use ColexNet+ to train high-quality multilingual embeddings ColexNet+ that are well-suited for transfer learning scenarios. Existing work on colexification patterns relies on annotated word lists. This limits scalability and usefulness in NLP. In contrast, we identify colexification patterns of more than 2,000 concepts across 1,335 languages directly from an unannotated parallel corpus. In our experiments, we first show that ColexNet has a high recall on CLICS, a dataset of crosslingual colexifications. We then evaluate ColexNet+ on roundtrip translation, verse retrieval and verse classification and show that our embeddings surpass several baselines in a transfer learning setting. This demonstrates the benefits of colexification for multilingual NLP.</small>
            </div>
          </div>

          <br>

        Xinpeng Wang, <b>Leonie Weissweiler</b>, Hinrich Schütze, Barbara Plank (2023). <i>How to Distill your BERT: An Empirical Study on the Impact of Weight Initialisation and Distillation Objectives</i>. Proceedings of the 61th Annual Meeting of the Association for Computational Linguistics. (<b>ACL</b>)<br>
          <a id="xinpeng_abstract_button" data-bs-toggle="collapse" href="#xinpeng_abstract" role="button" aria-expanded="false" aria-controls="xinpeng_abstract">Abstract</a>
          <a href="https://arxiv.org/pdf/2305.15032.pdf" target="_blank">PDF</a>
          <br>
          <div class="collapse" id="xinpeng_abstract" style="padding-top: 20px">
            <div class="card card-body">
              <small>Recently, various intermediate layer distillation (ILD) objectives have been shown to improve compression of BERT models via Knowledge Distillation (KD). However, a comprehensive evaluation of the objectives in both task-specific and task-agnostic settings is lacking. To the best of our knowledge, this is the first work comprehensively evaluating distillation objectives in both settings. We show that attention transfer gives the best performance overall. We also study the impact of layer choice when initializing the student from the teacher layers, finding a significant impact on the performance in task-specific distillation. For vanilla KD and hidden states transfer, initialisation with lower layers of the teacher gives a considerable improvement over higher layers, especially on the task of QNLI (up to an absolute percentage change of 17.8 in accuracy). Attention transfer behaves consistently under different initialisation settings. We release our code as an efficient transformer-based model distillation framework for further studies.</small>
            </div>
          </div>

